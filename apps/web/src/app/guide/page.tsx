const API_URL = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:3001/api';

interface TopicData {
  name: string;
  slug: string;
  icon: string | null;
  description: string | null;
}

async function getTopics(): Promise<TopicData[]> {
  try {
    const res = await fetch(`${API_URL}/guides/topics`, {
      next: { revalidate: 300 },
    });
    if (res.ok) {
      const data = await res.json();
      const topics = Array.isArray(data) ? data : data.data ?? [];
      if (topics.length > 0) {
        return topics.map((t: Record<string, unknown>) => ({
          name: String(t.name || ''),
          slug: String(t.slug || ''),
          icon: (t.icon || null) as string | null,
          description: (t.description || null) as string | null,
        }));
      }
    }
  } catch {
    // API unavailable
  }
  return [];
}

